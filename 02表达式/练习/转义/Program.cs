using System;

namespace 练习
{
    class Program
    {
        static void Main(string[] args)
        {
         
              Console.WriteLine("张三\n李四");//换行            
              Console.WriteLine("张三\b李四");//退格            
              Console.WriteLine("张三\\李四");//一个/            
              Console.WriteLine("张三\t李四");//水平制表符     
              Console.WriteLine("张三\"李四\"");//一个""
            
        }
    }
}
